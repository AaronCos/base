let sswh = {};
sswh.showSubject = function (data) {
    switch (data) {
        case 1:
            return '语文';
            break;
        case 2:
            return '数学';
            break;
        case 3:
            return '外语';
            break;
        case 4:
            return '物理';
            break;
        case 5:
            return '化学';
            break;
        case 6:
            return '历史';
            break;
        case 7:
            return '地理';
            break;
        case 8:
            return '生物';
            break;
        case 9:
            return '政治';
    }
}